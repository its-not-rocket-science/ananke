#!/usr/bin/env node

import { readFileSync, writeFileSync } from 'node:fs';
import { resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = resolve(fileURLToPath(new URL('.', import.meta.url)), '..');
const changelogPath = resolve(repoRoot, 'CHANGELOG.md');
const packageJsonPath = resolve(repoRoot, 'package.json');
const dashboardPath = resolve(repoRoot, 'docs/release-dashboard.md');

const args = new Set(process.argv.slice(2));
const shouldWrite = args.has('--write');
const shouldCheck = args.has('--check');

const changelog = readFileSync(changelogPath, 'utf8');
const pkg = JSON.parse(readFileSync(packageJsonPath, 'utf8'));

const releaseHeadingPattern = /^## \[(?<version>[^\]]+)] — (?<date>\d{4}-\d{2}-\d{2})$/gm;
const releases = [];
for (const match of changelog.matchAll(releaseHeadingPattern)) {
  const version = match.groups?.version;
  const date = match.groups?.date;
  if (!version || !date) continue;
  releases.push({ version, date });
}

if (releases.length === 0) {
  console.error('No release headings found in CHANGELOG.md');
  process.exit(1);
}

const latestChangelog = releases[0];
const packageVersion = pkg.version;

const issues = [];
if (latestChangelog.version !== packageVersion) {
  issues.push(`- Version mismatch: CHANGELOG latest is ${latestChangelog.version} but package.json is ${packageVersion}.`);
}

const consistency = issues.length === 0 ? 'PASS' : 'FAIL';
const issueText = issues.length === 0 ? '- None detected.' : issues.join('\n');

const maxRows = 10;
const releaseRows = releases.slice(0, maxRows)
  .map((release) => `| ${release.version} | ${release.date} |`)
  .join('\n');

const content = `# Release Dashboard\n\n> This file is generated by \`npm run generate-release-dashboard\`.\n> Do not edit manually. Update \`CHANGELOG.md\` and \`package.json\`, then regenerate.\n\n## Source of truth\n\n- \`CHANGELOG.md\` for release history and latest tagged release details.\n- \`package.json\` for the published package version.\n\n## Consistency status\n\n- Overall: **${consistency}**\n- Latest changelog version: **${latestChangelog.version}** (${latestChangelog.date})\n- Package version: **${packageVersion}**\n\n### Current inconsistencies\n\n${issueText}\n\n## Latest releases (from CHANGELOG)\n\n| Version | Date |\n| --- | --- |\n${releaseRows}\n\n## Automation design\n\n- Generate this dashboard from source data using \`tools/generate-release-dashboard.mjs\`.\n- Treat \`CHANGELOG.md\` and \`package.json\` as canonical inputs.\n- Regenerate dashboard in release-prep flows and whenever either input changes.\n\n## CI stale-file rule\n\n- CI runs \`npm run check-release-dashboard\`.\n- The check re-renders dashboard content in-memory and compares it with committed \`docs/release-dashboard.md\`.\n- If they differ, CI fails with a non-zero exit and asks contributors to run:\n  - \`npm run generate-release-dashboard\`\n`;

if (shouldWrite) {
  writeFileSync(dashboardPath, content, 'utf8');
  console.log(`Wrote ${dashboardPath}`);
}

if (shouldCheck) {
  const existing = readFileSync(dashboardPath, 'utf8');
  if (existing !== content) {
    console.error('docs/release-dashboard.md is stale. Run: npm run generate-release-dashboard');
    process.exit(1);
  }
  console.log('docs/release-dashboard.md is up to date.');
}

if (!shouldWrite && !shouldCheck) {
  process.stdout.write(content);
}
